public class Sum
{
  //attributes--AKA variables
  private int num1;
  private int num2;

  //this method adds two numbers
  public int sumOf()
  {
    return num1 + num2;
  }

  //constructor-- assigns values to my private variables
  public Sum(int number1, int number2)
  {
    num1 = number1;
    num2 = number2;
  }

}